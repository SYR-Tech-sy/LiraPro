function SectionGroup({
  title, icon, color, sections, startIndex,
}: {
  title: string; icon: React.ReactNode; color: string; sections: typeof APP_SECTIONS; startIndex: number;
}) {
  const [expanded, setExpanded] = useState(true);
  return (
    <div className="flex flex-col gap-3">
      <button type="button"
        onClick={() => setExpanded(e => !e)}
        className="flex items-center gap-2 p-3 rounded-xl border-2 transition-all"
        style={{ borderColor: color + '40', background: color + '08' }}
      >
        {icon}
        <span className="font-black text-sm flex-1 text-right" style={{ color }}>{title}</span>
        {expanded
          ? <ChevronUp className="w-4 h-4 flex-shrink-0" style={{ color }} />
          : <ChevronDown className="w-4 h-4 flex-shrink-0" style={{ color }} />}
      </button>
      {expanded && (
        <div className="flex flex-col gap-3">
          {sections.map((s, i) => (
            <motion.div key={i} initial={{ opacity: 0, y: 8 }} animate={{ opacity: 1, y: 0 }}
              transition={{ delay: i * 0.04 }}>
              <Card className="border-border shadow-sm">
                <CardContent className="p-4">
                  <h3 className="font-bold text-sm mb-2 flex items-center gap-1.5" style={{ color }}>
                    <span className="w-5 h-5 rounded-full text-white text-[9px] flex items-center justify-center font-bold flex-shrink-0"
                      style={{ background: color }}>{startIndex + i + 1}</span>
                    {s.title}
                  </h3>
                  <p className="text-xs text-foreground/70 dark:text-white leading-relaxed whitespace-pre-line">{s.body}</p>
                </CardContent>
              </Card>
            </motion.div>
          ))}
        </div>
      )}
    </div>
  );
}

export default function PrivacyPage() {
  const [, navigate] = useLocation();
  const search = useSearch();
  const { isSignedIn } = useUser();
  const params = new URLSearchParams(search);
  const forceISP = params.get('section') === 'isp';
  const forceVendor = params.get('vendor') === '1';

  const isISP = forceISP || (() => {
    try {
      const acc = localStorage.getItem('syp-wallet-account');
      return acc ? JSON.parse(acc).accountType === 'provider' : false;
    } catch { return false; }
  })();

  const isVendor = isSignedIn && (forceVendor || (() => {
    try {
      return localStorage.getItem('syp-is-vendor') === '1'
        || localStorage.getItem('syp-user-role') === 'vendor';
    } catch { return false; }
  })());

  return (
    <motion.div initial={{ opacity: 0, y: 16 }} animate={{ opacity: 1, y: 0 }} className="flex flex-col gap-4 pb-10">
      <div className="flex items-center gap-3 mb-2">
        <button type="button" onClick={() => navigate('/app/home')} className="p-2 rounded-full bg-secondary hover:bg-secondary/80 transition-colors">
          <ChevronRight className="w-5 h-5" />
        </button>
        <div className="flex items-center gap-2">
          <Shield className="w-5 h-5 text-primary" />
          <h2 className="text-lg font-bold">سياسة الخصوصية</h2>
        </div>
      </div>

      <Card className="border-primary/20 bg-primary/5">
        <CardContent className="p-4">
          <p className="text-xs text-foreground/70 dark:text-white text-center">
            آخر تحديث: مايو 2026 · LiraPro
          </p>
        </CardContent>
      </Card>

      <SectionGroup
        title="سياسة الخصوصية العامة"
        icon={<Shield className="w-4 h-4 flex-shrink-0" style={{ color: '#003C32' }} />}
        color="#003C32"
        sections={APP_SECTIONS}
        startIndex={0}
      />

      <SectionGroup
        title="سياسة الخدمات والرصيد"
        icon={<CreditCard className="w-4 h-4 flex-shrink-0" style={{ color: '#D20073' }} />}
        color="#D20073"
        sections={SERVICES_SECTIONS}
        startIndex={APP_SECTIONS.length}
      />

      {isVendor && (
        <SectionGroup
          title="سياسة مزودي الأسعار والتجار"
          icon={<Store className="w-4 h-4 flex-shrink-0" style={{ color: '#f59e0b' }} />}
          color="#f59e0b"
          sections={VENDOR_SECTIONS}
          startIndex={APP_SECTIONS.length + SERVICES_SECTIONS.length}
        />
      )}

      {isISP && (
        <SectionGroup
          title="سياسة مزودي الخدمات"
          icon={<Wifi className="w-4 h-4 flex-shrink-0" style={{ color: '#0284c7' }} />}
          color="#0284c7"
          sections={ISP_SECTIONS}
          startIndex={APP_SECTIONS.length + SERVICES_SECTIONS.length + (isVendor ? VENDOR_SECTIONS.length : 0)}
        />
      )}
    </motion.div>
  );
}
